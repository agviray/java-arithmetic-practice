public class ArithmeticOperators
{
    public static void main(String args[])
    {
        int a=14, b=5;
        int c=a/b;
        float cAsFloatDataType=(float)a/b;
        int r=a%b;

        System.out.println(c);
        System.out.println(r);
        System.out.println(cAsFloatDataType);
    }
}